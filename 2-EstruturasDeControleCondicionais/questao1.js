function vefiricarNumero(num) {
    if (num > 0) {
        console.log("O numero é positivo");
      return "Positivo";
    } else if (num < 0) {
        console.log("O numero é negativo");
      return "Negativo";
    } else {
        console.log("O numero é zero");
      return "Zero";
    }
  }
  